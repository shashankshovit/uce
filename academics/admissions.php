				<p><strong>The procedure for seeking admission in this college is as follows:</strong></p>
			
    <p>&nbsp; The process for seeking admission to this college is very simple. Since the college provides degrees in various disciplines,
	thus it has slightly different procedures in different courses. The college offers degrees in B.Tech, M.Tech and MBA. The admission procedures
	for these courses are as follows.</p>
		<br/>
	<h3>Bachelor of Technology (B.Tech)</h3>
	<p> There are three types of entries for B.Tech course in this college namely RPET, LEEP and direct admission.</p>
	
	<h4>RPET</h4>
	<p> First entry scheme is for the students who have just passed their 10+2
	examination. They have to appear for the <a href="http://techedu.rajasthan.gov.in" title="Click here to visit RPET website" target="_blank">
	Rajasthan Pre Engineering Test (RPET)</a> organised by Board of Technical Education, Jodhpur. The RPET then declares the results of the candidates
	according to their performance in the examination. The candidates now have to fill an option form indicating their preference order of colleges
	they wish to seek admission in. Based on the RPET results and option form, the board arranges counselling for the candidates and allots the colleges. 
	The candidate has to report to the respective college for further admission process.</p>
	
	<h4>LEEP</h4>
	<p>Another type of entry scheme to this college is Lateral Entry in Engineering and Pharmacy. The candidate who have just recieved the diploma 
	certificate is eligible for this type of entry scheme. After the applications are submitted by candidates, the board arranges counselling for the 
	diploma holders for college allotment. In this scheme, the candidate takes admission in the college in second year (IV semester) directly. </p>

	<h4>JEE (mains)</h4>
	<p>15% of the seats of each branch of this college is reserved for the candidates residing outside of Rajasthan. The admissions on these seats
	is sought on the basis of the merit list prepared by the board on the basis of JEE (mains) result. The candidates are advised to visit the 
	<a href="http://techedu.rajasthan.gov.in" title="click here to visit RPET website" target="_blank">	RPET website</a> for more details.</p>
	
	<h4>Direct Admission</h4>
	<p>The third and the last entry scheme is the direct admission. After the RPET has conducted its counselling and the colleges are alloted to the 
	candidates, and after the Upward Movement (refer to RPET website) process ; the college invites entries for direct admission for the remaining 
	vacant seats in the college for different branches. The candidate is free to participate in the direct admission counselling and compete over the 
	remaining seats in the college.</p>

	<h3>Master of Technology (M.Tech)</h3>
	<p>The admissions for M.Tech degree course is taken on the basis of <a href="http://www.rtu.ac.in/cam/cam.php" title="Centralised Admission for M.Tech page on RTU website" target="_blank">
	Centralised Admission for M.Tech (CAM)</a>. The candidate who has to apply for M.Tech in this college has to apply for CAM. A candidate is not
	allowed to take admission to this college by paying the fees directly. A merit list is prepared by the board for the candidated against their 
	alloted colleges on the basis of their applications submitted to the board.</p>
	
	<h3>Master of Business Administration (MBA)</h3>
	<p>Admissions to this course is sought through an examination namely Rajasthan Management Aptitude Test (RMAT) conducted annually. Based on this examination results,
	counselling is arranged for the candidates for the college allotment. This college is alloted on the basis of this counselling only.</p>
	
